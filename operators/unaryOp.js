//unary operators
let num1 = 5;
console.log(num1);
num1++;
console.log(num1++); //post increment
console.log(num1);
console.log(++num1); //pre increment
let num2 = 8;
console.log(num2--); //post decrement
console.log("num2= ", num2);

console.log(--num2); //pre decrement

console.log("num1= ", num1, "num2= ", num2);