//assignment operators
let num1 = 23;
let num2 = 12;
console.log(num1);

num1 = num2;
console.log(num1);

num1 += 3; //num1=num1+3
console.log(num1);

num1 -= 3;
console.log(num1);

num1 *= 2;
console.log(num1);

num1 /= 2;
console.log(num1);

num1 %= 5;
console.log(num1);

num2 **= 2; //num2^2
console.log(num2);